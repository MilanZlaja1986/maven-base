
public class FinalBarry {
	
	String nameOfBerry;
	int sizeOfBerry;
	int growthTimeOfBerry;
	


	public FinalBarry(String nameOfBerry, int sizeOfBerry, int growthTimeOfBerry) {
		this.nameOfBerry = nameOfBerry;
		this.sizeOfBerry = sizeOfBerry;
		this.growthTimeOfBerry = growthTimeOfBerry;
	}



	public String getNameOfBerry() {
		return nameOfBerry;
	}



	public void setNameOfBerry(String nameOfBerry) {
		this.nameOfBerry = nameOfBerry;
	}



	public int getSizeOfBerry() {
		return sizeOfBerry;
	}



	public void setSizeOfBerry(int sizeOfBerry) {
		this.sizeOfBerry = sizeOfBerry;
	}



	public int getGrowthTimeOfBerry() {
		return growthTimeOfBerry;
	}



	public void setGrowthTimeOfBerry(int growthTimeOfBerry) {
		this.growthTimeOfBerry = growthTimeOfBerry;
	}

}
